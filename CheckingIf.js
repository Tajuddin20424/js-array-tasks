const numbers = [1, 2, 3, 4, 5];
const name = "Taj";
const isStudent = true;
const scores = [89, 76, 95];
const age = 22;
const details = { firstName: "Taj", lastName: "uddin" };

console.log(`numbers is an array: ${Array.isArray(numbers)}`);
console.log(`name is an array: ${Array.isArray(name)}`);
console.log(`isStudent is an array: ${Array.isArray(isStudent)}`);
console.log(`scores is an array: ${Array.isArray(scores)}`);
console.log(`age is an array: ${Array.isArray(age)}`);
console.log(`details is an array: ${Array.isArray(details)}`);





